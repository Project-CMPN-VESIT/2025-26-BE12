def gen_insights(df_shots, df_bio, pid=None):
    label = f"Player {pid}" if pid else "All Players"
    insights = []

    if df_shots.empty:
        return insights

    # 1. Overall quality
    good_r = (df_shots["quality"]=="GOOD").mean()
    bad_r  = (df_shots["quality"]=="BAD").mean()
    if good_r > 0.6:
        insights.append(("good",  f"✅ {label}: Excellent overall — {good_r*100:.0f}% shots rated GOOD"))
    elif bad_r > 0.4:
        insights.append(("bad",   f"🔴 {label}: {bad_r*100:.0f}% shots rated BAD — technique needs work"))
    else:
        insights.append(("warn",  f"🟡 {label}: Consistent mid-level play — {good_r*100:.0f}% GOOD"))

    # 2. Weakest shot type
    shot_quality = df_shots.groupby("shot_type")["quality_score"].mean()
    if not shot_quality.empty:
        weakest = shot_quality.idxmin()
        weak_val = shot_quality.min()
        insights.append(("bad" if weak_val < 0.4 else "warn",
            f"⚠️ Weakest shot: **{weakest}** (avg quality score {weak_val:.2f}/1.0) — prioritise in training"))

    # 3. Best shot type
    best = shot_quality.idxmax()
    best_val = shot_quality.max()
    insights.append(("good",
        f"💪 Strongest shot: **{best}** (avg quality score {best_val:.2f}/1.0) — use this as go-to weapon"))

    # 4. Jump smash check
    smash = df_shots[df_shots["shot_type"]=="SMASH"]
    if not smash.empty:
        jump_smash_r = smash["jump_at_contact"].mean()
        if jump_smash_r < 0.3:
            insights.append(("bad",
                f"🦘 Only {jump_smash_r*100:.0f}% smashes were jump smashes — work on jump technique for more power"))
        else:
            insights.append(("good",
                f"🦘 {jump_smash_r*100:.0f}% smashes are jump smashes — great aerial game"))

    # 5. Elbow extension
    if "elbow_extension_norm" in df_shots.columns:
        avg_elbow = df_shots["elbow_extension_norm"].mean()
        if avg_elbow < 0.7:
            insights.append(("warn",
                f"💪 Avg elbow extension: {avg_elbow*100:.0f}% — extend arm fully at contact for more power"))

    # 6. Recovery
    avg_rec = df_shots["recovery_score"].mean()
    if avg_rec < 0.5:
        insights.append(("bad",
            f"🏃 Recovery score: {avg_rec:.2f} — returning to court centre after shots needs improvement"))
    else:
        insights.append(("good",
            f"🏃 Good court recovery score: {avg_rec:.2f} — returning well after shots"))

    # 7. Reaction time
    avg_react = df_shots["reaction_time_s"].mean()
    if avg_react > 4.0:
        insights.append(("warn",
            f"⏱️ Avg reaction time: {avg_react:.1f}s — work on transitioning between shots faster"))
    else:
        insights.append(("good",
            f"⏱️ Quick reactions: {avg_react:.1f}s avg between shots — excellent tempo"))

    # 8. Shuttle speed
    avg_spd = df_shots["shuttle_speed_kmh"].mean()
    if avg_spd < 60:
        insights.append(("warn",
            f"🏸 Avg shuttle speed: {avg_spd:.0f} km/h — work on generating more pace"))
    elif avg_spd > 85:
        insights.append(("good",
            f"🏸 High average speed: {avg_spd:.0f} km/h — powerful hitter"))

    # 9. Contact height for smash
    if not smash.empty:
        avg_contact = smash["contact_height_norm"].mean()
        if avg_contact < 0.5:
            insights.append(("bad",
                f"📍 Smash contact too low (avg {avg_contact:.2f}) — meet shuttle higher to increase angle"))

    # 10. Net shot quality
    netshot = df_shots[df_shots["shot_type"]=="NETSHOT"]
    if not netshot.empty:
        net_good = (netshot["quality"]=="GOOD").mean()
        if net_good < 0.4:
            insights.append(("bad",
                f"🕸️ Net play needs work — only {net_good*100:.0f}% NETSHOTS rated GOOD"))

    return insights

