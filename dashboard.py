c3.metric("🟡 AVERAGE",     f"{avg_pct}%")
c4.metric("🔴 BAD",         f"{bad_pct}%",   delta=f"-{bad_pct:.1f}%", delta_color="inverse")
c5.metric("Avg Speed",      f"{avg_speed} km/h")
c6.metric("Avg React Time", f"{avg_react}s")
c7.metric("Dominant Shot",  dominant_shot)


