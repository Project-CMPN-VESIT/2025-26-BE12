@st.cache_data
def load_data(bio_path, shot_path, court_path):
    bio   = pd.read_csv(bio_path)   if bio_path   else pd.DataFrame()
    shots = pd.read_csv(shot_path)  if shot_path  else pd.DataFrame()
    court = pd.read_csv(court_path) if court_path else pd.DataFrame()
    return bio, shots, court

